package tugas_kedua;



public class Tugas_Kedua_2 {
    
    public static void main(String[] args) {
    int a, b;
    a = 6;
    b = 3;

    System.out.println("Hasil A: " +a); //Hasilnya 6 
    a += b;
    System.out.println("Hasil += : "+a); //Hasilnya 9 
    a -= b;
    System.out.println("Hasil -= : " +a); //Hasilnya 6 
    a *= b;
    System.out.println("Hasil *= : "+a); //Hasilnya 18
    a /= b;
    System.out.println("Hasil /= : "+a); //Hasilnya 6 
    a %= b;
    System.out.println("Hasil %= : "+a); //Hasil 0 
    
    
    
    }
    
    
}
